package Week15;

/**
 * Mountain class for ArrayLists complex object example
 * @author neilr
 */

public class Mountain {
    
    private String name;
    private int height;

    /**
     * @return the name
     */
    public String getName(){
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name){
        this.name = name;
    }

    /**
     * @return the height
     */
    public int getHeight(){
        return height;
    }

    /**
     * @param height the height to set
     */
    public void setHeight(int height){
        this.height = height;
    }
}
