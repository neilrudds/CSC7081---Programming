package Week10;

/**
 * Performs simple mathematical calculations
 * 
 * @author neilr
 */

public class SimpleCalc {
	
	/**
	* Adds two numbers together and outputs the 
	* result to screen
	* @param num1 the first number
	* @param num2 the 2nd number
	*/
	public void add(int num1, int num2) {
		System.out.println(num1+num2);
	}
	
	/**
	* Subtracts two numbers and outputs the 
	* result to screen
	* @param num1 the first number
	* @param num2 the 2nd number
	*/
	public void subtract(int num1, int num2) {
		System.out.println(num1-num2);
	}
	
	/**
	* Multiply's two numbers together and outputs the 
	* result to screen
	* @param num1 the first number
	* @param num2 the 2nd number
	*/
	public void multiply(int num1, int num2) {
		System.out.println(num1*num2);
	}
	
	/**
	* Divide's two numbers together and outputs the 
	* result to screen
	* @param num1 the first number
	* @param num2 the 2nd number
	*/
	public void divide(int num1, int num2) {
		System.out.println(num1/num2);
	}
	
}
